export function getDevotion(date) {
  const devotion = dailyDevotions.filter((devotion) => {
    return devotion.date === date;
  });

  // if there is not a devotion for the date, return err msg
  return devotion[0]
    ? devotion
    : "<p style='text-align:center;'><i>we are currently working on the devotion. Would be uploaded soon! <strong>Yours in Christ.</strong></p>";
}

